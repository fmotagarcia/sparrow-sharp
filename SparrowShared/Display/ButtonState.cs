namespace Sparrow.Display
{
    /// <summary>
    ///  An enum that provides constant values for the states of the <see cref="Button"/> class. 
    /// </summary>
    public enum ButtonState
    {
        /// <summary>
        /// The Button's default state
        /// </summary>
        Up,
        
        Down,
        
        Over,
        /// <summary>
        /// The button is disabled
        /// </summary>
        Disabled
    }
}